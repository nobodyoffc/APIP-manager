package Users;

public class User {

	private String id;
	private Sesson sesson;
	private long fch;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Sesson getSesson() {
		return sesson;
	}
	public void setSesson(Sesson sesson) {
		this.sesson = sesson;
	}
	public long getFch() {
		return fch;
	}
	public void setFch(long fch) {
		this.fch = fch;
	}
	
	
}
